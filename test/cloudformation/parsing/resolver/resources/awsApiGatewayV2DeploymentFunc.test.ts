import log from 'loglevel';
import { awsApiGatewayV2DeploymentFunc } from '../../../../../src/cloudformation/parsing/resolver/resources/awsApiGatewayV2DeploymentFunc';
import { ResolvingContext } from '../../../../../src/cloudformation/parsing/types/types';
import { generateAlphaNumeric, resolveString } from '../../../../../src/cloudformation/parsing/utils/helper-utils';
import { CloudFormationResource } from '../../../../../src/cloudformation/types/cloudformation-model';

// Mock the helper functions so we can control their outputs.
jest.mock('../../../../../src/cloudformation/parsing/utils/helper-utils', () => ({
    generateAlphaNumeric: jest.fn(() => 'MOCKED_ID'),
    resolveString: jest.fn((_value: unknown, _propertyName: string, _ctx: ResolvingContext) => 'resolvedApiId'),
}));

/**
 * Creates a fresh mock ResolvingContext for testing.
 *
 * @returns A mock ResolvingContext.
 */
function createMockContext(): ResolvingContext {
    return {
        originalTemplate: {},
        lookupMapPreProcessed: {},
        generatedIds: new Set(),
        lookupMapDynamic: {},
        currentPath: [],
        addName: jest.fn(),
        popName: jest.fn(() => ''),
        getCurrentPath: jest.fn(() => ''),
        hasParameterName: jest.fn(() => false),
        getParameter: jest.fn(),
        addParameter: jest.fn(),
        addGeneratedId: jest.fn(),
        isIdExists: jest.fn(() => false),
        getRegion: jest.fn(() => 'us-east-1'),
        getPartition: jest.fn(() => 'aws'),
        getAccountId: jest.fn(() => '123456789012'),
        getAZs: jest.fn(() => ['us-east-1a', 'us-east-1b']),
    } as unknown as ResolvingContext;
}

describe('awsApiGatewayV2DeploymentFunc', () => {
    let mockCtx: ResolvingContext;
    let resource: CloudFormationResource;

    beforeEach(() => {
        // Create fresh context and resource for every test.
        mockCtx = createMockContext();
        resource = {
            Properties: {
                ApiId: 'dummyApiId',
            },
        } as CloudFormationResource;

        // Ensure _id and _arn are not preset.
        delete resource._id;
        delete resource._arn;

        // Reset helper mocks.
        (generateAlphaNumeric as jest.Mock).mockClear();
        (resolveString as jest.Mock).mockClear();
    });

    describe('idGenFunc', () => {
        /**
         * Generates a new ID if not already set.
         */
        it('should generate a new ID if not already set', () => {
            const id = awsApiGatewayV2DeploymentFunc.idGenFunc(
                'AWS::ApiGatewayV2::Deployment',
                'TestLogicalId',
                resource,
                mockCtx,
            );
            expect(id).toBe('MOCKED_ID');
            expect(resource._id).toBe('MOCKED_ID');
            expect(generateAlphaNumeric).toHaveBeenCalledWith(6, mockCtx);
        });

        /**
         * Returns the existing ID if it is already set.
         */
        it('should return the existing ID if already set', () => {
            resource._id = 'existingId';
            const id = awsApiGatewayV2DeploymentFunc.idGenFunc(
                'AWS::ApiGatewayV2::Deployment',
                'TestLogicalId',
                resource,
                mockCtx,
            );
            expect(id).toBe('existingId');
            expect(generateAlphaNumeric).not.toHaveBeenCalled();
        });
    });

    describe('refFunc', () => {
        /**
         * Returns the ID generated by idGenFunc.
         */
        it('should return the ID generated by idGenFunc', () => {
            const id = awsApiGatewayV2DeploymentFunc.refFunc(
                'AWS::ApiGatewayV2::Deployment',
                'TestLogicalId',
                resource,
                mockCtx,
            );
            expect(id).toBe('MOCKED_ID');
        });
    });

    describe('getAttFunc', () => {
        /**
         * Returns the ID when the key is "DeploymentId".
         */
        it('should return the ID when key is "DeploymentId"', () => {
            const id = awsApiGatewayV2DeploymentFunc.getAttFunc(
                'AWS::ApiGatewayV2::Deployment',
                'DeploymentId',
                'TestLogicalId',
                resource,
                mockCtx,
            );
            expect(id).toBe('MOCKED_ID');
        });

        /**
         * Warns and returns the ID for an unsupported attribute key.
         */
        it('should warn and return the ID when an unsupported key is passed', () => {
            const warnSpy = jest.spyOn(log, 'warn').mockImplementation(() => {});
            const id = awsApiGatewayV2DeploymentFunc.getAttFunc(
                'AWS::ApiGatewayV2::Deployment',
                'UnsupportedKey',
                'TestLogicalId',
                resource,
                mockCtx,
            );
            expect(id).toBe('MOCKED_ID');
            expect(warnSpy).toHaveBeenCalledWith(
                `Passed key UnsupportedKey for AWS::ApiGatewayV2::Deployment, with logicalId=TestLogicalId is not supported, id will be returned`,
                resource,
                mockCtx,
            );
            warnSpy.mockRestore();
        });
    });

    describe('arnGenFunc', () => {
        /**
         * Generates and assigns a new ARN if none exists.
         */
        it('should generate and assign a new ARN if none exists', () => {
            const arn = awsApiGatewayV2DeploymentFunc.arnGenFunc(
                'AWS::ApiGatewayV2::Deployment',
                'TestLogicalId',
                resource,
                mockCtx,
            );

            // Verify that resolveString was used to resolve ApiId.
            expect(resolveString).toHaveBeenCalledWith(
                'dummyApiId',
                'AWS::ApiGatewayV2::Deployment.Properties.ApiId',
                mockCtx,
            );

            // Expected ARN format: arn:{partition}:apigateway:{region}::/apis/{resolvedApiId}/deployments/{id}
            const expectedArn = 'arn:aws:apigateway:us-east-1::/apis/resolvedApiId/deployments/MOCKED_ID';
            expect(arn).toBe(expectedArn);
            expect(resource._arn).toBe(expectedArn);
        });

        /**
         * Returns the existing ARN if it is already set.
         */
        it('should return the existing ARN if it is already set', () => {
            resource._arn = 'existingArn';
            const arn = awsApiGatewayV2DeploymentFunc.arnGenFunc(
                'AWS::ApiGatewayV2::Deployment',
                'TestLogicalId',
                resource,
                mockCtx,
            );
            expect(arn).toBe('existingArn');
        });
    });
});
